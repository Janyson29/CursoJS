// Aritmético 
// + Adição / Concatenação
const num1 = 15;
const num2 = 5;
console.log(num1 + num2); // --> Adição

// const num1 = '5'; --> Concatenação

// - Subtração

console.log(num1 - num2); // --> Subtração 

// * Multiplicação

console.log(num1 * num2); // --> Multiplicação

// / Divisão

console.log(num1 / num2); // --> Divisão

// ** Exponenciação / Potenciação

console.log(num1 ** num2); // --> Exponenciação / Potenciação

// % Resto da divisão

console.log(num1 % num2); // --> Resto da divisão

